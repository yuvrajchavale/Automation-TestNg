package javaPractice_24;

public class Operators {

	public static void main(String[] args) {
		int a , b , c;
		a = 100;
		b = 300;
		c = 500;
		
		a++; // Increment Operator
		b+=25; 
		c-=50; // Decrement Operator
		System.out.println("Updated value a : " + a);
		System.out.println("Updated value b : "+ b);
		System.out.println("Updated value c : "+ c );
     
		
	}

}
